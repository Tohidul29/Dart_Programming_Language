void main(){
  //declare variable:
  int a = 19;
  int b = 10;

  //using ternary operator: condition? (statement 1) : (statement 2)
  a > b? print('$a is greater than  $b') : print('$a is smaller than $b');
  a % 2 == 0? print('$a is an even number'):print('$a is an odd number'); 
  b % 2 == 0? print('$b is an even number'):print('$b is an odd number'); 
}